#include "funcs.h"

double Add(double num1, double num2) // Add num1 and num2
{
    return num1 + num2;
}
double Subtrac(double num1, double num2) // Subtract num2 from num1
{
    return num1 - num2;
}
double Multiplicate(double num1, double num2) // Multiplicate num1 with num2
{
    return num1 * num2;
}
double Divide(double num1, double num2) // Divide num1 with num2
{
    return num1 / num2;
}